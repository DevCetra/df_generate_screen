// ░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░

List<String>? dartObjToList(DartObject? obj) {
  return (letListOrNull<String>(obj?.toStringValue()) ??
          obj?.toListValue()?.map((e) => e.toStringValue()))
      ?.nonNulls
      .toList();
}
